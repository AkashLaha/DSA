package com.akash;

public class Primitives {
    public static void main(String args[]){
        System.out.println();
        //by default every decimal no. is of type double, thats why (f) is added at last of float value.
        float m=20.2f;
        int a=20;
        //for large int values (long) is used
        //by default every decimal no. is of type int, thats why (L) is added at last of long value.
        long s=2323555566777788L;
        //for large decimal values (double) is used
        double v=232222242433.232333445;
        char b='r';
        boolean j=false;
        //String is not a primitive
        String g="akash";
    }
}
